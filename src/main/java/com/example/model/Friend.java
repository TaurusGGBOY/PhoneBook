package com.example.model;

public class Friend {

	private String id;
	private String friend_name;
	private String friend_tel;
	private String friend_sex;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getfriend_name() {
		return friend_name;
	}

	public void setfriend_name(String friend_name) {
		this.friend_name = friend_name;
	}

	public String getfriend_tel() {
		return friend_tel;
	}

	public void setfriend_tel(String friend_name) {
		this.friend_tel = friend_name;
	}

	public String getfriend_sex() {
		return friend_sex;
	}

	public void setfriend_sex(String friend_name) {
		this.friend_sex = friend_name;
	}

}